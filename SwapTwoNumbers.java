package com.bridgelab;

public class SwapTwoNumbers {
    public static void main(String[] args) {
        int a = 11, b = 12;
        int c;
        System.out.println(" a = " + a + " b = " + b);

        c = a;
        a = b;
        b = c;
        System.out.println(" a= " + a + " b= " + b);

    }
}
